              maken zones de bediening veel eenvoudiger.</h4>

            <h3><b>Verdieping</b></h3>

            <h4 class="space"> Je kunt ook kamers groeperen in een zone, zoals 'begane grond' of 'eerste verdieping'.
              Je kan dan bijvoorbeeld met één swipe op het touchscreen alle lampen op een verdieping uitzetten of een andere Scene toekennen.</h4>

            <h4 class="space">De Hue-app update is zowel beschikbaar voor iOS als Android.
              De Zones-optie bevindt zich nog wel in de testfase en werkt nog niet met alle functies,

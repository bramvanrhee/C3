            <h1>Google Stadia: zo werkt de nieuwe gamedienst</h1>
            <h4 class="space2"><small><i>11 juni 2019</i></small></h4>

            <h4 class="space"><b>Google heeft deze week zijn langverwachte gamestreamingdienst aangekondigd.
              Hoe werkt het streamen van games met Google Stadia? En wat gaat het kosten?</b></h4>

            <video src="../../MP4/GoogleStadia.mp4" controls class="img-responsive"></video>

            <h3><b>Hoe werkt Stadia precies?</b></h3>

            <h4 class="space">Eigenlijk heel simpel: in plaats van een spelcomputer naast je tv,
              worden games afgespeeld op de servers van Google.
              Samen met videokaartenmaker AMD heeft Google speciale videochips ontwikkeld,
              waardoor Stadia bij de start een kracht van 10,7 teraflops heeft. Ter vergelijking:
              de PS4 Pro heeft 4,2 teraflops en de Xbox One X heeft 6,0 teraflops.</h4>

            <h4 class="space">Die games worden dan naar je tv, laptop, tablet of telefoon gestreamd, met een resolutie van maximaal 4K,
              op 60 beeldjes per seconde in HDR. Om te zorgen dat er zo min mogelijk vertraging optreedt tussen de controller en de game,
              is de bijbehorende Stadia Controller via wifi verbonden met de servers van Google.
              Dat zou voor een vertraging moeten zorgen die vergelijkbaar is met die van een PS4 of Xbox One die naast een tv staat.</h4>

            <h3><b>Hoe snel moet je internet zijn?</b></h3>

            <h4 class="space">Google zegt dat een verbinding van 'ongeveer 25 Mbps' snel genoeg is voor games op 1080p met 60 beeldjes per seconde.
              Tegen Kotaku zei Google Stadia-hoofd Phil Harrison dat 4K mogelijk is vanaf 30 Mbps.
              In Nederland ligt de gemiddelde snelheid van vaste internetaansluitingen op ruim 35 Mbps.
              Bij een test enkele maanden geleden merkte The Verge via een vaste verbinding geen gestotter op, via wifi wel.
              Polygon vond de compressie van het geluid vrij agressief.</4>

            <h4 class="space">Digital Foundry, die gamebeelden haast wetenschappelijk analyseert,
              vindt dat bij 4K-beelden op een tv-scherm nog wel te zien is dat er compressie wordt gebruikt,
              ten opzichte van games die lokaal worden afgespeeld. Op een kleiner laptopscherm zou de compressie minder zichtbaar zijn.</h4>

            <h4 class="space">Het verschil zal vermoedelijk even groot zijn als dat tussen een 4K-film op Netflix en een 4K Blu-ray.
              Ook tussen die twee is een verschil zichtbaar, maar de vraag is of dat verwaarloosbaar is of niet.
              Ook duurt het nog een tijdje voor Stadia beschikbaar is.
              Bovendien kan Google zijn systemen voortdurend verbeteren nadat de dienst beschikbaar is gekomen.</h4>

            <h3><b>Welke games zijn er beschikbaar?</b></h3>

            <h4 class="space">We weten zeker dat Assassin’s Creed: Odyssey, DOOM: Eternal,
              de recente Tomb Raider-games en basketbalgame NBA2K naar Stadia komen.
              Het is goed denkbaar dat andere games van die ontwikkelaars ook naar Stadia komen, maar dat is nog niet toegezegd.</h4>

            <h4 class="space">Daarnaast voert Google hoogstwaarschijnlijk nog onderhandelingen met tal van andere uitgevers van games.
              Google heeft beloofd later dit jaar meer duidelijk te maken over het aanbod games op Stadia.</h4>

            <h3><b>Op welke apparaten werkt het?</b></h3>

            <h4>Google zegt dat 'Stadia werkt op elk apparaat waar YouTube op beschikbaar is'.
              Stadia werkt in ieder geval op de nieuwste Chromecast, in de Chrome-browser,
              op Chromebooks en op nieuwe Android-telefoons en tablets met Chrome.
              Het is nog niet duidelijk of Google aparte apps voor Stadia uitbrengt.
              Of de gamedienst beschikbaar komt op apparaten van Apple is ook nog de vraag.</h4>

            <h3><b>Waarom komt Google hier mee?</b></h3>

            <h4 class="space">De gamingmarkt is één van de meest lucratieve ter wereld en gamestreaming zit er al jaren aan te komen.
              Google is dankzij zijn krachtige datacenters en goede verbindingen bij uitstek in staat om zo'n dienst succesvol op te zetten.
              Alles valt of staat namelijk bij snelheid en beschikbaarheid. Google heeft genoeg schaalbaarheid om games snel af te spelen,
              en voor genoeg gamers tegelijk.</h4>

            <h3><b>Wat kost Stadia en wanneer komt het beschikbaar?</b></h3>

            <h4 class="space">Google heeft nog niks gezegd over prijzen.
              Het is niet eens duidelijk of spelers straks los voor games betalen, zoals bij spelcomputers,
              of dat er een abonnement komt waarbij voor een vast bedrag per maand het volledige aanbod kan worden gespeeld.
              Een abonnement is aannemelijk, maar Google zal dat eerst nog moeten overleggen met uitgevers.
              De prijs van de Stadia Controller is ook nog niet bekend, maar een prijs tussen de 50 en 100 euro lijkt realistisch.</h4>

            <h3><b>Gaat Google ook zelf games maken?</b></h3>

            <h4 class="space">Ja, net als andere makers van gameplatforms komt ook Google met eigen games.
              Die worden ontwikkeld onder leiding van Jade Raymond,
              voorheen producent bij Ubisoft en verantwoordelijk voor de totstandkoming van de Assassin's Creed-serie.</h4>

            <h3><b>Wie werken er nog meer aan zulke diensten?</b></h3>

            <h4 class="space">PlayStation en Nvidia hebben al langer gamestreamingdiensten, maar minder krachtig.
              Microsoft werkt aan het vergelijkbare xCloud, waarbij ook Microsoft zijn eigen Azure-datacenters kan inzetten.</h4>

            <h4 class="space">Ook Amazon zou aan een gamestreamingdienst werken, ook met zijn eigen datacenters.
              Apple werkt volgens geruchten eveneens aan een nieuwe abonnementsdienst rond games.</h4>
